  <h3>Receita de Pão Caseiro</h3>
  <h4>Ingredientes</h4>
  <ul>
    <li>1 kg de farinha de trigo aproximadamente</li>
    <li>1 e 1/2 xícara de leite morno</li>
    <li>1/2 xícara de água morna</li>
    <li>1/2 xícara de óleo</li>
    <li>4 colheres (sopa) de açúcar</li>
    <li>1 colher (sopa) de sal</li>
    <li>2 ovos</li>
    <li>30 g de fermento biológico fresco ou 10 g de fermento biológico seco</li>
  </ul>
  <h4>Modo de Preparo</h4>
  <p>
      Em uma tigela, dissolva o fermento no açúcar e acrescente
      o sal, os ingredientes líquidos, os ovos e misture muito bem.
    Acrescente aos poucos a farinha até formar uma massa macia e sove
    bem a massa.Deixe a massa descansar por aproximadamente 1 hora.
      Após o crescimento, divida a massa, enrole da forma que desejar,
      coloque nas formas e deixe crescer até dobrar de volume.
    Leve para assar em forno médio (200° C), preaquecido, por aproximadamente 30 minutos.
    Retire o pão do forno e pincele leite para a casca ficar mais macia.
  </p>
